controller.left.onEvent(ControllerButtonEvent.Pressed, function () {
    MC.setVelocity(-50, 0)
    info.changeScoreBy(2)
})
controller.B.onEvent(ControllerButtonEvent.Pressed, function () {
    if (info.score() == 0) {
        MC.setVelocity(0, -50)
        pause(350)
        MC.setVelocity(0, 50)
    }
    if (info.score() == 2) {
        MC.setVelocity(-80, -50)
        pause(400)
        MC.setVelocity(-30, 50)
    }
    if (info.score() == 1) {
        MC.setVelocity(80, -50)
        pause(400)
        MC.setVelocity(30, 50)
    }
})
controller.left.onEvent(ControllerButtonEvent.Released, function () {
    MC.setVelocity(0, 50)
    info.changeScoreBy(-2)
})
controller.A.onEvent(ControllerButtonEvent.Repeated, function () {
    if (info.score() == 0) {
        MC.setVelocity(0, 0)
    }
    if (info.score() == 2) {
        MC.setVelocity(-100, 0)
    }
    if (info.score() == 1) {
        MC.setVelocity(100, 0)
    }
})
